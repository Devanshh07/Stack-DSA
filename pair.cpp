#include<bits/stdc++.h>
#include<iostream>

using namespace std;

template <typename T>
class pair{
   T x;
    T  y;

    public:
    void setX(T  s){
        this->x = s;
    }
    T  getX(){
        return x;
    }
    void setY(T  q){
        this->y = q;
    }
    T  getY(){
        return y;
    }

};
